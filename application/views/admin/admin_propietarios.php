var exst = '0'; var xid ='0';
$("#ci").keyup(function(){
    
    $("#nm").val(''); $("#ap").val(''); $("#em").val(''); $("#telf").val('');

    if($(this).val().length >= 8){
    var url = "<?=site_url();?>Rubi_admin/get_ci_data";
    var data  = new Object();
    data["ci"] = $(this).val();
    
    $.ajax({data:data, type: "POST", dataType: "json",url: url,})
    .done(function( data, textStatus, jqXHR ) {
          var newData = JSON.stringify(data) 
         var res = jQuery.parseJSON(newData); 
         if(res.error_msg == '0'){ 
          $("#ci").removeClass("exist").addClass("noexist");
          exst = '0';

         }else{
          $("#ci").removeClass("noexist").addClass("exist");
          $("#nm").val(res.nm);
          $("#ap").val(res.ap);
          $("#em").val(res.em);
          $("#telf").val(res.telf);
          $("#s_rol").val(res.role);
          exst = '1';
          xid = res.xid;
         }
    })
    .fail(function( jqXHR, textStatus, errorThrown ) {
     if ( console && console.log ) {
         console.log( "La solicitud a fallado: " +  textStatus);
     }
    }); 
  }
});

